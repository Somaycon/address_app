class AddressEntity {
  final String postalCode;
  final String street;
  final String neighborhood;
  final String stateCode;
  final String state;
  final String city;

  AddressEntity({
    required this.postalCode,
    required this.street,
    required this.neighborhood,
    required this.stateCode,
    required this.state,
    required this.city,
  });
}
